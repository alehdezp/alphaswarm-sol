def generate_tier2_scaffold(
    finding: Finding,
    project_config: Optional[ProjectConfig] = None,
    source_pragma: Optional[str] = None,
) -> TestScaffold:
    """
    Generate Tier 2 test scaffold with smart import resolution.

    Attempts to:
    - Resolve import paths based on project structure
    - Match pragma version to source contract
    - Include forge-std/Test.sol import
    - Provide basic setUp() structure

    Expected compile rate: 30-40%

    Args:
        finding: The finding to generate test for
        project_config: Optional project configuration for import resolution
        source_pragma: Optional pragma from source contract

    Returns:
        TestScaffold with smart template content
    """
    try:
        # Extract finding info
        finding_id = finding.id or "UNKNOWN"
        finding_id_safe = _make_safe_identifier(finding_id)
        category = _extract_category(finding)
        category_safe = _make_safe_identifier(category)
        test_contract_name = f"Test_{finding_id_safe}_{category_safe}"

        # Location and contract info
        location = finding.location or "unknown"
        target_contract_type = _extract_contract_name(location)

        # Determine pragma version
        pragma_version = suggest_test_pragma(source_pragma)

        # Build imports
        imports, confidence = _build_tier2_imports(
            project_config,
            location,
            target_contract_type,
        )

        # Generate attack steps
        attack_steps = _generate_attack_steps(category)

        # Generate content
        content = TIER2_TEMPLATE.format(
            timestamp=datetime.now().isoformat(),
            finding_id=finding_id,
            finding_id_safe=finding_id_safe,
            finding_title=finding.title or "Unknown Vulnerability",
            severity=finding.severity.value if finding.severity else "unknown",
            category=category,
            category_safe=category_safe,
            location=location,
            description=_indent_text(finding.description or "No description"),
            test_contract_name=test_contract_name,
            target_contract_type=target_contract_type,
            pragma_version=pragma_version,
            imports=imports,
            attack_steps=attack_steps,
            confidence=confidence,
        )

        filename = f"{test_contract_name}.t.sol"

        return TestScaffold(
            finding_id=finding_id,
            tier=2,
            content=content,
            filename=filename,
            confidence=confidence,
        )

    except Exception as e:
        # Fall back to Tier 1 on any error
        scaffold = generate_tier1_scaffold(finding)
        scaffold.compilation_error = f"Tier 2 failed: {e}, fell back to Tier 1"
        return scaffold


def _build_tier2_imports(
    project_config: Optional[ProjectConfig],
    location: str,
    target_contract_type: str,
) -> tuple[str, float]:
    """
    Build import statements for Tier 2.

    Returns:
        Tuple of (imports string, confidence score)
    """
    imports = []
    confidence = 0.4  # Base confidence for Tier 2

    # Always try to add forge-std
    if project_config:
        resolver = ImportResolver(project_config)
        forge_import = resolver.suggest_forge_std_import()
        imports.append(forge_import)

        # If forge-std resolves cleanly, boost confidence
        if not forge_import.startswith("//"):
            confidence += 0.1

        # Try to resolve target contract import
        if location and location != "unknown":
            # Extract just the file path from location
            file_path = location.split(":")[0] if ":" in location else location

            if file_path:
                # Try to build import path
                try:
                    contract_path = Path(file_path)
                    import_stmt = resolver.resolve_contract_import(contract_path)
                    imports.append(import_stmt)

                    # Boost confidence if resolved without TODO
                    if "TODO" not in import_stmt:
                        confidence += 0.1
                except Exception:
                    imports.append(f'// TODO: Import target contract\n// import "{file_path}";')
            else:
                imports.append(f"// TODO: Import {target_contract_type}")
        else:
            imports.append(f"// TODO: Import {target_contract_type}")

    else:
        # No project config - use defaults
        imports.append('import "forge-std/Test.sol";')
        imports.append(f"// TODO: Import {target_contract_type}")
        confidence = 0.25  # Lower confidence without project config

    return "\n".join(imports), min(confidence, 0.6)  # Cap at 60%


