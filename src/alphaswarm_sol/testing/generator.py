"""
Test Scaffold Generator (Tasks 4.5, 4.6)

Generates test scaffolds for verifying security findings.

Philosophy:
- Tier 1: MUST NEVER FAIL - always produces useful output
- Tier 2: Attempts smart resolution - ~30-40% compile rate
- Tier 3: Aspirational full tests - <10% success rate

This module generates test files that help auditors verify vulnerabilities.
It does NOT attempt to automatically exploit or verify findings.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from datetime import datetime
from enum import Enum
from pathlib import Path
from typing import Dict, List, Optional, Any

from alphaswarm_sol.enterprise.reports import Finding, Severity
from alphaswarm_sol.testing.detection import ProjectConfig, ProjectType
from alphaswarm_sol.testing.remappings import ImportResolver
from alphaswarm_sol.testing.pragma import suggest_test_pragma
from alphaswarm_sol.testing.tiers import TestTier


@dataclass
class TestScaffold:
    """
    Generated test scaffold.

    Attributes:
        finding_id: ID of the finding this tests
        tier: Generation tier (1, 2, or 3)
        content: Solidity test file content
        filename: Suggested filename
        compiled: Whether compilation was attempted/succeeded
        compilation_error: Error message if compilation failed
        confidence: Estimated likelihood this compiles (0.0 to 1.0)
    """

    finding_id: str
    tier: int
    content: str
    filename: str
    compiled: Optional[bool] = None
    compilation_error: Optional[str] = None
    confidence: float = 0.0

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary for serialization."""
        return {
            "finding_id": self.finding_id,
            "tier": self.tier,
            "filename": self.filename,
            "compiled": self.compiled,
            "compilation_error": self.compilation_error,
            "confidence": self.confidence,
            "content_length": len(self.content),
        }


