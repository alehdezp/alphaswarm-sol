def generate_tier1_scaffold(finding: Finding) -> TestScaffold:
    """
    Generate Tier 1 test scaffold.

    MUST NOT FAIL. Always produces output.

    Tier 1 provides:
    - Test file structure with valid Solidity syntax
    - Vulnerability description in comments
    - Attack vector explanation for the category
    - TODO markers for manual completion
    - Helper contract templates

    Args:
        finding: The finding to generate test for

    Returns:
        TestScaffold with template content
    """
    try:
        # Safe extraction - handle missing/empty data gracefully
        finding_id = finding.id or "UNKNOWN"
        finding_id_safe = _make_safe_identifier(finding_id)

        # Extract category from finding
        # The Finding class doesn't have a category field, extract from location or title
        category = _extract_category(finding)
        category_safe = _make_safe_identifier(category)

        test_contract_name = f"Test_{finding_id_safe}_{category_safe}"

        # Extract location info
        location = finding.location or "unknown"
        target_contract_type = _extract_contract_name(location)

        # Build attack steps from category
        attack_steps = _generate_attack_steps(category)

        # Generate content
        content = TIER1_TEMPLATE.format(
            timestamp=datetime.now().isoformat(),
            finding_id=finding_id,
            finding_id_safe=finding_id_safe,
            finding_title=finding.title or "Unknown Vulnerability",
            severity=finding.severity.value if finding.severity else "unknown",
            category=category,
            category_safe=category_safe,
            location=location,
            description=_indent_text(finding.description or "No description provided"),
            recommendation=_indent_text(finding.recommendation or "No recommendation provided"),
            test_contract_name=test_contract_name,
            target_contract_type=target_contract_type,
            attack_steps=attack_steps,
        )

        # Generate filename
        filename = f"{test_contract_name}.t.sol"

        return TestScaffold(
            finding_id=finding_id,
            tier=1,
            content=content,
            filename=filename,
            confidence=1.0,  # Tier 1 always "succeeds" as a template
        )

    except Exception as e:
        # Tier 1 MUST NOT FAIL - return minimal template on any error
        return _fallback_scaffold(finding, str(e))


def _fallback_scaffold(finding: Finding, error: str) -> TestScaffold:
    """Generate minimal fallback scaffold when main generation fails."""
    finding_id = getattr(finding, "id", "UNKNOWN") or "UNKNOWN"
    content = f'''// SPDX-License-Identifier: MIT
pragma solidity ^0.8.0;

// VKG TEST SCAFFOLD - FALLBACK
// Generated due to error: {error}
// Finding ID: {finding_id}

// TODO: Implement test manually
contract Test_Fallback {{
    function test_vulnerability() public {{
        // TODO: Implement test
        revert("Not implemented");
    }}
}}
'''
    return TestScaffold(
        finding_id=finding_id,
        tier=1,
        content=content,
        filename="Test_Fallback.t.sol",
        confidence=0.5,
    )


def _make_safe_identifier(s: str) -> str:
    """Convert string to safe Solidity identifier."""
    if not s:
        return "Unknown"

    # Replace non-alphanumeric with underscore
    safe = "".join(c if c.isalnum() else "_" for c in s)

    # Remove consecutive underscores
    while "__" in safe:
        safe = safe.replace("__", "_")

    # Remove leading/trailing underscores
    safe = safe.strip("_")

    # Ensure doesn't start with number
    if safe and safe[0].isdigit():
        safe = "X" + safe

    return safe or "Unknown"


def _extract_contract_name(location: str) -> str:
    """Extract contract name from location string."""
    if not location:
        return "TargetContract"

    # Location might be "Contract.sol:functionName:line" or just a path
    path_part = location.split(":")[0] if ":" in location else location

    # Get filename without extension
    try:
        name = Path(path_part).stem
        return name if name else "TargetContract"
    except Exception:
        return "TargetContract"


def _extract_category(finding: Finding) -> str:
    """Extract vulnerability category from finding."""
    # Try to infer from title or description
    title = (finding.title or "").lower()
    desc = (finding.description or "").lower()
    combined = f"{title} {desc}"

    categories = [
        ("reentrancy", ["reentran", "re-entran", "callback"]),
        ("access_control", ["access", "permission", "owner", "admin", "auth", "unauthorized"]),
        ("oracle", ["oracle", "price", "chainlink", "twap"]),
        ("mev", ["mev", "sandwich", "frontrun", "backrun", "slippage"]),
        ("dos", ["dos", "denial", "unbounded", "loop", "gas"]),
        ("overflow", ["overflow", "underflow", "arithmetic"]),
        ("signature", ["signature", "ecrecover", "eip712", "replay"]),
    ]

    for cat_name, keywords in categories:
        for keyword in keywords:
            if keyword in combined:
                return cat_name

    return "unknown"


def _indent_text(text: str, prefix: str = "    // ") -> str:
    """Indent multi-line text for comments."""
    if not text:
        return prefix + "No information provided"

    lines = text.strip().split("\n")
    return f"\n{prefix}".join(lines)


def _generate_attack_steps(category: str) -> str:
    """Generate attack step comments based on category."""
    category = category.lower()

    attack_patterns = {
        "reentrancy": '''        // Attack Pattern: Reentrancy
        // 1. Attacker deploys contract with malicious receive()/fallback()
        // 2. Attacker calls vulnerable withdraw() function
        // 3. Before state is updated, attacker re-enters via callback
        // 4. Attacker drains funds by repeated re-entry''',

        "access_control": '''        // Attack Pattern: Missing Access Control
        // 1. Identify privileged function without access check
        // 2. Call function as non-privileged user
        // 3. Verify state was modified (should have been rejected)''',

        "oracle": '''        // Attack Pattern: Oracle Manipulation
        // 1. Manipulate oracle price (flash loan, sandwich, etc.)
        // 2. Call function that relies on manipulated price
        // 3. Profit from price discrepancy''',

        "mev": '''        // Attack Pattern: MEV Vulnerability
        // 1. Monitor mempool for target transaction
        // 2. Front-run with price manipulation
        // 3. Execute original transaction at worse price
        // 4. Back-run to extract profit''',

        "dos": '''        // Attack Pattern: Denial of Service
        // 1. Identify resource consumption vector (loops, storage, external calls)
        // 2. Trigger resource exhaustion
        // 3. Verify function becomes unusable''',

        "overflow": '''        // Attack Pattern: Arithmetic Overflow/Underflow
        // 1. Identify arithmetic operation without SafeMath (pre-0.8.0)
        // 2. Provide inputs that cause overflow/underflow
        // 3. Verify unexpected state change''',

        "signature": '''        // Attack Pattern: Signature Vulnerability
        // 1. Obtain or forge a valid signature
        // 2. Replay signature on different chain/contract
        // 3. Or: Submit malformed signature that passes ecrecover''',
    }

    # Find matching pattern or use generic
    for key, pattern in attack_patterns.items():
        if key in category:
            return pattern

    return '''        // Attack Pattern: Generic
        // 1. Identify preconditions for vulnerability
        // 2. Execute attack transaction
        // 3. Verify vulnerability was exploited'''


