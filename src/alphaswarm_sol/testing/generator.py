def generate_scaffolds(
    findings: List[Finding],
    tier: TestTier = TestTier.TIER_1_TEMPLATE,
    project_config: Optional[ProjectConfig] = None,
) -> List[TestScaffold]:
    """
    Generate test scaffolds for multiple findings.

    Args:
        findings: List of findings to generate tests for
        tier: Target tier level
        project_config: Optional project configuration

    Returns:
        List of TestScaffold instances
    """
    scaffolds = []

    for finding in findings:
        if tier == TestTier.TIER_1_TEMPLATE:
            scaffold = generate_tier1_scaffold(finding)
        elif tier == TestTier.TIER_2_SMART:
            scaffold = generate_tier2_scaffold(finding, project_config)
        else:
            # Tier 3 not implemented - fall back to Tier 2
            scaffold = generate_tier2_scaffold(finding, project_config)
            scaffold.tier = 3
            scaffold.confidence *= 0.25  # Much lower confidence

        scaffolds.append(scaffold)

    return scaffolds


def write_scaffold_to_file(
    scaffold: TestScaffold,
    output_dir: Path,
) -> Path:
    """
    Write scaffold to file in output directory.

    Args:
        scaffold: The scaffold to write
        output_dir: Directory to write to

    Returns:
        Path to written file
    """
    output_dir.mkdir(parents=True, exist_ok=True)
    output_path = output_dir / scaffold.filename
    output_path.write_text(scaffold.content)
    return output_path
